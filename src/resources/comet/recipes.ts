  "",
  "{ \"name\": \"comet_ask\", \"arguments\": { \"task_id\": \"<news>\", \"prompt\": \"…\" } }",
  "{ \"name\": \"comet_ask\", \"arguments\": { \"task_id\": \"<docs>\", \"prompt\": \"…\" } }",
