  "",
  "```json",
  "{ \"name\": \"comet_ask\", \"arguments\": {",
  "  \"prompt\": \"What is the capital of Iceland?\"",
  "}}",
  "```",
  "",
  "## 1b. One-shot agentic / browsing task",
  "",
  "Phrase the prompt as an INSTRUCTION so Comet uses its browser:",
  "",
  "```json",
  "{ \"name\": \"comet_ask\", \"arguments\": {",
  "  \"prompt\": \"Use your browser to navigate to https://news.ycombinator.com and report the top 5 story titles with their points.\"",
  "}}",
  "```",
  "",
  "Bad version (Comet may answer from cached knowledge):",
  "",
  "```json",
  "{ \"name\": \"comet_ask\", \"arguments\": { \"prompt\": \"top 5 HN stories\" } }",
  "```",
  "",
  "## 2. Multi-turn conversation in the same tab",
  "",
