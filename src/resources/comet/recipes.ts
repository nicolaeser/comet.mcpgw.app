  "",
  "```json",
  "{ \"name\": \"comet_connect\", \"arguments\": { \"label\": \"thread\" } }",
  "{ \"name\": \"comet_ask\", \"arguments\": { \"task_id\": \"<id>\",",
  "  \"prompt\": \"Summarize the latest research on RLHF.\",",
  "  \"closeAfter\": false } }",
  "{ \"name\": \"comet_ask\", \"arguments\": { \"task_id\": \"<id>\",",
  "  \"prompt\": \"Now compare it to constitutional AI.\",",
  "  \"closeAfter\": false } }",
  "{ \"name\": \"comet_task_close\", \"arguments\": { \"task_id\": \"<id>\" } }",
  "```",
  "",
  "Forgetting `closeAfter:false` on a turn auto-closes the tab and ends the",
  "conversation. To start a fresh chat in the SAME task's tab without losing",
