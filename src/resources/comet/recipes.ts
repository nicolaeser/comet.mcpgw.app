const resource = defineResource({
  uri: URI,
  name: "comet_recipes",
  title: "Comet Bridge — Workflow Recipes",
  description:
    "Copy-pasteable JSON-RPC examples for the 11 most common workflows: " +
    "one-shot, multi-step, parallel, polling, drive-a-page, debug, seed auth, " +
    "mobile emulation, block trackers, eval, retained async results.",
  mimeType: "text/markdown",
  annotations: {
    audience: ["assistant", "user"],
    priority: 0.85,
  },
  async read() {
    return {
      contents: [{ uri: URI, mimeType: "text/markdown", text: content }],
    };
  },
});

export default resource;
